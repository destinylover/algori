A = input()

if A == "A0":
    print("4.0")
elif A == "A+":
    print("4.3")
elif A == "A-":
    print("3.7")
elif A == "B+":
    print("3.3")
elif A == "B0":
    print("3.0")
elif A == "B-":
    print("2.7")
elif A == "C+":
    print("2.3")
elif A == "C0":
    print("2.0")
elif A == "C-":
    print("1.7")
elif A == "D+":
    print("1.3")
elif A == "D0":
    print("1.0")
elif A == "D-":
    print("0.7")
elif A == "F":
    print("0.0")
else:
    print("잘못입력했습니다.")
